
public class Bounds {
	
	private int cols;
	private int rows;
	
	public Bounds(int rows, int cols) {
		this.rows = rows;
		this.cols = cols;
	}

	public int getCols() {
		return cols;
	}

	public void setCols(int cols) {
		this.cols = cols;
	}

	public int getRows() {
		return rows;
	}

	public void setRows(int rows) {
		this.rows = rows;
	}
}
